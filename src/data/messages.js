// classifyMsg は後方で定義されるが、呼び出しは UI 実行時のためクロージャ解決で OK
const _pickTemplate = (templates, msg) => {
  // classifyMsg を遅延参照（循環定義順の回避）
  const mt = (typeof classifyMsg === "function") ? classifyMsg(msg) : "その他";
  return (templates[mt] || templates["その他"] || ((s)=>s))(msg);
};
for (const key of Object.keys(_MSG_DEF)) {
  const def = _MSG_DEF[key];
  def.frank   = (msg)=>_pickTemplate(def.templates.frank,  msg);
  def.normal  = (msg)=>_pickTemplate(def.templates.normal, msg);
  def.polite  = (msg)=>_pickTemplate(def.templates.polite, msg);
  def.pattern = (msg)=>_pickTemplate(def.templates.normal, msg);
}
