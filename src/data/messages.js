// 並び順は「特異・短いキー」を先に、複合語に飲み込まれやすいものを後に。
// 先勝ちなので、謝罪・感謝・告白などを先に置いて誤分類を減らす。
export const MSG_CLASSIFY = {
  謝罪:    ["ごめん","申し訳","すまない","悪かった","反省","謝りたい"],
  感謝:    ["ありがとう","感謝","おかげで","助かった","嬉しかった","嬉しい"],
  告白:    ["好き","気になってる","大切","いつも気になって","あなたのことが","好意","付き合"],
  承認:    ["すごい","さすが","かっこいい","素敵","尊敬","頼りになる","センスいい","才能"],
  批判:    ["なんで","どうして","ありえない","最悪","遅い","ちゃんとして","なんであなたは"],
  連絡:    ["今どこ","何してる","返事して","既読","見てる？","忙しい？","元気にしてる"],
  誘い:    ["デート","会いたい","会おう","一緒に行","遊ぼう","来ない","来てよ","どこか行","行こう"],
  依頼:    ["お願い","手伝って","助けて","やってほしい","頼みたい","できる？","してほしい"],
};

export const classifyMsg = (msg) => {
  for (const [type, words] of Object.entries(MSG_CLASSIFY)) {
    if (words.some(w => msg.includes(w))) return type;
  }
  return "その他";
};


